// 배열을 구조 분해해서 값을 뽑아낼 수도 있다. 배열의 첫 번째 원소를 변수에 대입하고 싶다고 가정하자.
const [firstAnimal] = ["캥거루", "웜뱃", "코알라"];

console.log(firstAnimal);

// 불필요한 값을 콤마(,)를 사용해 생략하는 리스트 매칭(list matching)을 사용할 수도 있다. 변수에 대입하지 않고 무시하고 싶은 원소의
// 위치에 콤마를 넣으면 리스트 매칭을 사용하는 것이다. 앞에서 본 것과 같은 배열에서 최초의 두 원소를 콤마로 대치하면 다음과 같다.
const [, , thirdAnimal] = ["캥거루", "웜뱃", "코알라"];

console.log(thirdAnimal);
